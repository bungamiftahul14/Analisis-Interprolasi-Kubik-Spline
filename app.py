# =========================================================
# ALGORITMA KUBIK SPLINE MANUAL 
# =========================================================
def cubic_spline_manual(x, y, x_pred):
    n = len(x) - 1
    h = np.diff(x)

    A = np.zeros((n + 1, n + 1))
    B = np.zeros(n + 1)

    A[0, 0] = 1
    A[n, n] = 1

    for i in range(1, n):
        A[i, i - 1] = h[i - 1]
        A[i, i] = 2 * (h[i - 1] + h[i])
        A[i, i + 1] = h[i]
        B[i] = 3 * ((y[i + 1] - y[i]) / h[i] -
                    (y[i] - y[i - 1]) / h[i - 1])

    c = np.linalg.solve(A, B)

    a = y[:-1]
    b = np.zeros(n)
    d = np.zeros(n)

    for i in range(n):
        b[i] = (y[i + 1] - y[i]) / h[i] - h[i] * (2 * c[i] + c[i + 1]) / 3
        d[i] = (c[i + 1] - c[i]) / (3 * h[i])

    for i in range(n):
        if x[i] <= x_pred <= x[i + 1]:
            dx = x_pred - x[i]
            return a[i] + b[i]*dx + c[i]*dx**2 + d[i]*dx**3

    return None


def spline_curve(x, y, xx):
    return [cubic_spline_manual(x, y, xi) for xi in xx]


